class CollectibleObject extends DrawableObject {
  
    /**
     * Creates a CollectibleObject with specified coordinates.
     * @param {number} x - The x-coordinate of the collectible object.
     * @param {number} y - The y-coordinate of the collectible object.
     */
    constructor(x, y) {
        super();
        this.x = x;
        this.y = y;
    }

}